# 24389. 2의 보수
# 1s, 512MB

# 2의 보수: not -> +1
# 32bit 기준, 처음 수와 2의 보수의 서로 다른 비트 수=?
# N: 10^9

# 0000 0000 0000 0000 0000 0000 0001 0110 (22)
# 1111 1111 1111 1111 1111 1111 1110 1010 (-22)

# 0000 0000 0000 0000 0000 0000 0000 0001 (2)
# 1111 1111 1111 1111 1111 1111 1111 1111 (-2)

N = int(input())
ans = 0

for i in range(32):
    if (N & (1<<i)) != ((-N) & (1<<i)):
        ans += 1

print(ans)